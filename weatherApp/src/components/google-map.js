import React, { Component } from 'react';

class GoogleMap extends Component {
  componentDidMount() {
    new google.maps.Map(this.refs.map, {
      zoom: 12,
      center: {
        lat: this.props.lat,
        lng: this.props.lon
      }
    });
  }
  render() {
    //this.maps.refs will give us a direct access to the rendered div
    return <div ref="map" />
  }
}

export default GoogleMap;
